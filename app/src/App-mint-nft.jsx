export default function App() {
    const [account, setAccount] = useState('');

  const handleAccountsChanged = (accounts) => {
    if (accounts.length === 0) {
      console.log('Please connect to MetaMask.');
    } else {
      setAccount(accounts[0]);
    }
  };

  const handleMint = async () => {
    const to = '0x7d89Cb57D758350211D1a173A8884A24511dDC65';
    const provider = new ethers.providers.Web3Provider(window.ethereum, "any");
    const signer = provider.getSigner();
    const contract = new ethers.Contract("0x4CED5CD011841aAff70Bf8E81F5cEa3191E10D8E", ERC721ABI, signer);
    await contract.mintNFT(to, "https://api.pudgypenguins.io/lil/30", 50);
}

  useEffect(() => {
    if (window.ethereum) {
      window.ethereum
        .request({ method: 'eth_requestAccounts' })
        .then(handleAccountsChanged)
        .catch((err) => {
          console.error(err);
        });

      window.ethereum.on('accountsChanged', handleAccountsChanged);
    } else {
      alert('MetaMask is not installed. Please consider installing it: https://metamask.io/download.html');
    }
  }, []);

  return <button onClick={handleMint}>Mint</button>;
}
